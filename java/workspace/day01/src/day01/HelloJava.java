package day01;

/*
	자바 프로그램의 최소단위 = 클래스. 맨 바깥의 중괄호는 클래스 범위를 지정하는 블록. 
	public class 파일이름 {
	}
	클래스의 이름은 소스파일의 이름(HelloJava)과 동일해야 하며, 첫글자는 반드시 영어여야 한다.
*/
public class HelloJava {

	//메인 메서드. 프로그램의 시작점을 의미하는 블록. 프로그램에 반드시 존재, 맨 앞에 존재.
	public static void main(String[] args) {
		// Hello Java 가 출력된다.
		/*
	     * 콘솔에 문장을 출력하기 위한 명령어.
	     * +쌍따옴표로 묶으면 문자열(문장)이 만들어진다.
	     */
		System.out.println("Hello Java");
	}
}
